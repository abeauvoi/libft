#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (!n || !s1 || !s2)
		return (!n ? 1 : NO_INPUT);
	while (*s1 && *s1 == *s2 && n-- > 1)
	{
		++s1;
		++s2;
	}
	return (*s1 == *s2 ? 1 : 0);
}
