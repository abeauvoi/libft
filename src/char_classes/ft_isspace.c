int			ft_isspace(int c)
{
	unsigned char	uc;

	uc = (unsigned char)c;
	return (uc == ' '
			|| uc == '\f'
			|| uc == '\n'
			|| uc == '\r'
			|| uc == '\t'
			|| uc == '\v');
}
