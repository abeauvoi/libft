#include "libft.h"

int		ft_xdigittoint(char c)
{
	uint8_t	d;

	d = (uint8_t)(c - '0');
	if (d < 10)
		return (d);
	d = (uint8_t)(c - 'a');
	if (d < 6)
		return (d + 10);
	d = (uint8_t)(c - 'A');
	if (d < 6)
		return (d + 10);
	return (-1);
}
