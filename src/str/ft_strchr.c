#include "libft.h"

char 		*ft_strchr(const char *str, int c)
{
	char	b;

	b = c;
	while (str[0] != '\0' && str[0] != b)
		++str;
	if (str[0] == b)
		return ((char *)str);
	return (NULL);
}
