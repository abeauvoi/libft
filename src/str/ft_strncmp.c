#include "libft.h"

int				ft_strncmp(const char *s1, const char *s2, size_t n)
{
	while (n-- > 0 && *us1 == *us2)
	{
		if (n == 0 || *us1 == '\0')
			return (0);
		++us1;
		++us2;
	}
	return (0);
}
