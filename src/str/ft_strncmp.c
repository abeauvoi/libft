#include "libft.h"

int				ft_strncmp(const char *s1, const char *s2, size_t n)
{
	while (n-- > 0 && *s1 == *s2)
	{
		if (n == 0 || *s1 == '\0')
			return (0);
		++s1;
		++s2;
	}
	return (0);
}
