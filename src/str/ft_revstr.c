#include "libft.h"

void		ft_revstr(char *s)
{
	char	*t;
	char	swap;

	t = s + ft_strlen(s) - 1;
	while (t > s)
	{
		swap = *t;
		*t = *s;
		*s = swap;
		++s;
		--t;
	}
}
