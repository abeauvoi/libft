#include "libft.h"

size_t	ft_strnlen(const char *s, size_t maxlen)
{
	size_t	len;

	len = 0;
	while (len < maxlen)
	{
		++len;
		if (s[0] == '\0')
			break ;
	}
	return (len);
}
