#include "libft.h"

uint8_t		ft_nbrlen_b8(uint64_t val)
{
	if (val < 010)
		return (1);
	if (val < 0100)
		return (2);
	if (val < 01000)
		return (3);
	if (val < 01000000000000ull)
	{
		if (val < 0100000000)
		{
			if (val < 01000000)
			{
				if (val < 010000)
					return (4);
				return (5 + (val >= 0100000));
			}
			return (7 + (val >= 010000000));
		}
		if (val < 010000000000ull)
			return (9 + (val >= 01000000000));
		return (11 + (val >= 0100000000000ull));
	}
	return (12 + ft_nbrlen_b8(val / 01000000000000ull));
}
