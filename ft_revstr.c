#include "libft.h"

void		ft_revstr(char *s)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = ft_strlen(s) - 1;
	while (i < j)
	{
		s[i] ^= s[j];
		s[j] ^= s[i];
		s[i++] ^= s[j--];
	}
}
