int		ft_digittoint(char c)
{
	int	d;

	d = c - '0';
	if ((unsigned)d < 10)
		return (d);
	d = c - 'a';
	if ((unsigned)d < 6)
		return (d + 10);
	d = c - 'A';
	if ((unsigned)d < 6)
		return (d + 10);
	return (-1);
}
